            <p>The Serengeti National Park is undoubtedly one of Africa's most iconic wilderness areas, a place where nature's drama unfolds on an epic scale. Spanning over 14,750 square kilometers of pristine plains, woodlands, and riverine forests, this UNESCO World Heritage Site offers an unparalleled safari experience that draws visitors from around the globe.</p>

            <h2>The Great Migration: Nature's Greatest Spectacle</h2>
            <p>At the heart of the Serengeti's appeal is the Great Migration, an endless cycle of life that sees over 1.5 million wildebeest, 400,000 zebra, and 200,000 gazelle journey across the ecosystem in search of fresh grazing.</p>
            
            <div class="highlight-box">
                <div class="highlight-icon">
                    <i class="fas fa-quote-left"></i>
                </div>
                <p>Witnessing the river crossings during the migration is perhaps the most dramatic wildlife spectacle on the planet. The desperation of thousands of animals plunging into crocodile-infested waters creates moments of intense drama and raw nature.</p>
            </div>

            <p>The migration follows a fairly predictable annual pattern:
            <ul>
                <li><strong>December-April:</strong> Calving season in the southern Serengeti</li>
                <li><strong>May-July:</strong> Movement toward the Western Corridor</li>
                <li><strong>August-September:</strong> Mara River crossings</li>
                <li><strong>October-November:</strong> Return journey to the south</li>
            </ul>
            </p>

            <h2>Exceptional Big Five Viewing Opportunities</h2>
            <p>The Serengeti offers some of Africa's best opportunities to see the Big Five in their natural habitat. The park's healthy populations of lions, leopards, elephants, buffalo, and rhinos (though black rhinos are rarely seen and critically endangered) provide unforgettable wildlife encounters.</p>

