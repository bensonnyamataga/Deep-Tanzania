            <h2>Diverse Ecosystems and Landscapes</h2>
            <p>Contrary to popular belief, the Serengeti isn't just endless plains. The park encompasses several distinct habitats, each with its own character and wildlife:</p>

            <div class="image-with-caption">
                <img src="img/serengeti-land-scape-view.jpg" alt="Diverse Serengeti Landscapes">
                <p>The varied landscapes of Serengeti National Park support an incredible diversity of wildlife</p>
            </div>

            <h3>Seronera Valley</h3>
            <p>Known as the "Big Cat Capital of Africa," this central region with its kopjes (rock formations) and riverine forests offers exceptional predator sightings year-round.</p>

            <h3>Western Corridor</h3>
            <p>Home to the Grumeti River, this region hosts dramatic river crossings during the migration and is known for its massive crocodiles and black-and-white colobus monkeys.</p>

            <h3>Northern Serengeti</h3>
            <p>The most remote and least visited area, featuring rolling hills and larger trees. This is where the famous Mara River crossings occur between July and October.</p>

            <div class="highlight-box secondary">
                <div class="highlight-icon">
                    <i class="fas fa-lightbulb"></i>
                </div>
                <p><strong>Local Insight:</strong> The name 'Serengeti' comes from the Maasai word 'siringet,' meaning 'endless plains.' The Maasai people have lived in harmony with this ecosystem for centuries, and their cultural heritage adds another layer of richness to any visit.</p>
            </div>

            <h2>World-Class Birdwatching Destination</h2>
            <p>With over 500 bird species recorded, the Serengeti is a paradise for bird enthusiasts. From the large and charismatic (like ostriches and kori bustards) to the small and colorful (like lilac-breasted rollers and superb starlings), the avian diversity will impress even casual observers.</p>

            <h2>Conservation Success Story</h2>
            <p>By visiting the Serengeti, you directly contribute to one of Africa's great conservation success stories. Tourism revenue funds anti-poaching efforts, community development projects, and scientific research that helps protect this incredible ecosystem for future generations.</p>

